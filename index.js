function add(a, b) {
    return a + b;
  }

  function subtract(a, b) {
    return a - b;
  }

  function multiply(a, b) {
    return a * b;
  }

  function divide(a, b) {
    return a / b;
  }
  function increment(a) {
    return ++a;
  }
  function decrement(b) {
    return --b;
  }
  function parseInteger(d) {
    return parseInt(d);
  }
  function makeInt(a) {
    return parseInt(a, 10);
  }
  function preserveDecimal(z) {
    return parseFloat(z);
  }